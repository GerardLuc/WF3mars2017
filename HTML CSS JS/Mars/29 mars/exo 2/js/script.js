var myNumber = 45;

// Egalité SIMPLE : verifier la valeur de la variable)
console.log( myNumber == 45);
console.log( myNumber == '45');

// Inégalité SIMPLE
console.log( myNumber != 45);
console.log( myNumber != "45");
console.log( myNumber != 12);
console.log( myNumber != "12");

// Egalité STRICTE: verifier la valeur et le type de la variable

console.log( myNumber === 45 );

// Inégalité STRICTE: verifier la valeur et le type de la variable
console.log( myNumber !== 45);
console.log( myNumber !== "45");

// Superieur/inferieur
console.log( myNumber > 46 )
console.log( myNumber < 46 )

// Superieur ou égal / inferieur ou égal
console.log( myNumber >= 12 )
console.log( myNumber <= 12 )

console.log( myNumber <= 45 )
console.log( myNumber >= 45 )
